import java.util.*;
import java.io.*;

import org.xml.sax.*;
import org.xml.sax.helpers.DefaultHandler;

import javax.xml.parsers.SAXParserFactory;
import javax.xml.parsers.SAXParser;

public final class LevelLoader extends DefaultHandler {
	
	protected CoordVector actual_coords;
	protected int l, x, y;
	
	protected GameItem gi;
	protected String type, sub_type, state, name;
	protected int vis_type;
	protected boolean take_able, walk_on_able;
	protected long weight;
	
	protected HashMap hm;
	
	protected boolean inMapTile;
	
	protected LevelLoader()
	{
		hm=new HashMap();
	}
	
	public static final HashMap loadLevel(String file_name)
	{
		LevelLoader handler=new LevelLoader();
		
		SAXParserFactory factory = SAXParserFactory.newInstance();
		try {
			
			// Parse the input
			SAXParser saxParser = factory.newSAXParser();
			saxParser.parse( new File(file_name), handler);
			
		}
		catch (SAXParseException spe)
		{
			System.out.println("** Parsing error" 
					+ ", line " + spe.getLineNumber()
					+ ", uri " + spe.getSystemId());
			System.out.println("   " + spe.getMessage() );
			
//			Exception  x = spe;
//			if (spe.getException() != null)
//				x = spe.getException();
//			x.printStackTrace();
		}
		catch (SAXException sxe) {
			// Error generated by this application
			// (or a parser-initialization error)
			Exception  x = sxe;
			if (sxe.getException() != null)
				x = sxe.getException();
			x.printStackTrace();
			
//			Exception  ex = sxe;
//			if (sxe.getException() != null)
//				x = sxe.getException();
//			x.printStackTrace();
		}
		
		finally
		{
			return handler.getHashMap();
		}
	}
	
	public HashMap getHashMap()
	{
		return hm;
	}
	
	public void startDocument()
	throws SAXException
	{
		
	}
	
	public void endDocument()
	throws SAXException
	{
		
	}
	
	public void startElement(String namespaceURI,
		String lName, // local name
		String qName, // qualified name
		Attributes attrs)
	throws SAXException
	{
		String eName = lName; // element name
		if ("".equals(eName)) eName = qName; // namespaceAware = false
		
		if (eName.equals("maptile"))
		{
			reinitCoords();
		
			if (attrs != null) {
				for (int i = 0; i < attrs.getLength(); i++) {
					String aName = attrs.getLocalName(i); // Attr name 
					if ("".equals(aName)) 
						aName = attrs.getQName(i);
						
					if (aName.equals("l"))
					{
						l=Integer.parseInt(attrs.getValue(i));
					}
					
					else if (aName.equals("x"))
					{
						x=Integer.parseInt(attrs.getValue(i));
					}
					
					else if (aName.equals("y"))
					{
						y=Integer.parseInt(attrs.getValue(i));
					}
				}
			}
			
			inMapTile=true;
			
			actual_coords=new CoordVector(l, x, y);
		}
		
		else if (eName.equals("object") && inMapTile)
		{
			reinitItem();
		
			if (attrs != null) 
			{
				for (int i = 0; i < attrs.getLength(); i++) {
					String aName = attrs.getLocalName(i); // Attr name 
					if ("".equals(aName)) 
						aName = attrs.getQName(i);
				
					if (aName.equals("type"))
					{
						type=attrs.getValue(i);
					}
			
					else if (aName.equals("sub_type"))
					{
						sub_type=attrs.getValue(i);
					}
		
					else if (aName.equals("state"))
					{
						state=attrs.getValue(i);
					}
					
					else if (aName.equals("name"))
					{
						name=attrs.getValue(i);
					}
					
					else if (aName.equals("vis_type"))
					{
						vis_type=Integer.parseInt(attrs.getValue(i));
					}
					
					else if (aName.equals("take_able"))
					{
						take_able=Boolean.getBoolean(attrs.getValue(i));
					}
					
					else if (aName.equals("walk_on_able"))
					{
						walk_on_able=Boolean.getBoolean(attrs.getValue(i));
					}
					
					else if (aName.equals("weight"))
					{
						weight=Long.parseLong(attrs.getValue(i));
					}
				}
			}
		
			gi=new GameItem(type, sub_type, state, name, vis_type, take_able,
			walk_on_able, weight);
		}
		
		else if (eName.equals("object") && !inMapTile)
		{
			System.out.println("<object>-Tags dürfen nur in <maptile>-Tags benutzt werden");
		}
	}
	
	public void endElement(String namespaceURI,
		String sName, // simple name
		String qName  // qualified name
		)
		throws SAXException
	{
		String eName = sName; // element name
		if ("".equals(eName)) eName = qName; // namespaceAware = false
		
		if (eName.equals("maptile"))
		{
			hm.put(actual_coords, gi);
			inMapTile=false;
		}
	}
	
	protected void reinitCoords()
	{
		actual_coords=null;
		l=0;
		x=0;
		y=0;
	}
	
	protected void reinitItem()
	{
		gi=null;
		type="";
		sub_type="";
		state="";
		name="";
		vis_type=0;
		take_able=false;
		walk_on_able=false;
		weight=0;
	}
}
